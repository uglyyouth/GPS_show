
$(document).ready(function(){
    $("#queryByCode").bind("click",
        function() {
            if($.trim($("#codename").val())!="")
            {
                $.ajax({
                    url:"/gps/getmessagebycode.do",
                    type:"GET",
                    dataType:"json",
                    data:{
                        "code":$("#codename").val()
                    },
                    async:false,
                    success: function(data){
                        var codedata = data;
                        init(codedata);
                        console.log(codedata);
                    }
                });
            }else{
                alert('查询车牌号不能为空');
            }
        });

});

